// Definition: Collections of key-value pairs where the keys can be of any type. The insertion order is preserved.

// Example: Storing and retrieving data based on custom keys.

// Use Case: Maps are useful for scenarios where the order of insertion is important and quick key-based retrieval is required.

// Code Example:


let map = new Map();
map.set("key1", "value1");
map.set("key2", "value2");
console.log(map.get("key1")); // Output: value1