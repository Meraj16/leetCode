// Definition: Last-In-First-Out (LIFO) data structures where elements are added and removed from one end called the "top" of the stack.

// Example: Managing function calls, undo/redo operations.

// Use Case: Stacks are useful when you need to keep track of the order of operations and perform operations in a specific order.

// Code Example:

let stack = [];
stack.push(1);
stack.push(2);
console.log(stack.pop()); // Output: 2